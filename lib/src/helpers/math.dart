///
/// Check if the [input] is greater than [value]
///
bool isValueGreaterThan(double input, double value) {
  return input > value;
}

///
/// Check if the [input] is greater than or equal to [value]
///
bool isValueGreaterThanEqualTo(double input, double value) {
  return input >= value;
}

///
/// Check if the [input] is less than [value]
///
bool isValueLessThan(double input, double value) {
  return input < value;
}

///
/// Check if the [input] is less than or equal to [value]
///
bool isValueLessThanEqualTo(double input, double value) {
  return input <= value;
}
